function oddOrEven(number) {
    if (number % 2 == 0) {
        return 'The number is even.';
    }
    else {
        return 'The number is odd.'
    }
}

console.log(oddOrEven(3));
console.log(oddOrEven(8));
console.log(oddOrEven(89));
